s = 'hello world'

# a banch of string methods

# capitalize string
print(s.capitalize())

# make all to upper case
print(s.upper())
 
# swap case
print(s.swapcase())

# get length 
print(len(s))

# replace
print(s.replace('world', 'everyone'))

# count
sub = 'h'
print(s.count(sub))

# starts with
print(s.startswith('hello'))

# ends with
print(s.endswith('d'))

# split into list
print(s.split())

#find position
print(s.find('r'))

# is all alphanumerical 
print(s.isalnum())

# is all alphabetic
print(s.isalpha())

# is all numeric 
print(s.isnumeric())





