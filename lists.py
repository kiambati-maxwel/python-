fruits = ['apples', 'oranges', 'grapes', 'pears']

# get a value 
print(fruits[1])

# get length
print(len(fruits))

# append to list 
fruits.append('Mangos')

# remove from list
fruits.remove('grapes')

# insert into position
fruits.insert(2, 'strawberries')

# remove with pop
fruits.pop(2)

# reverse list
fruits.reverse()

# sort
fruits.sort()

# reverse sort
fruits.sort(reverse=False)

 
print(fruits)